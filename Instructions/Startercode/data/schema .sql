CREATE TABLE COUNTRY_LAT_LONG
  (	COUNTRY_ID VARCHAR(10) PRIMARY KEY,
	LATITUDE FLOAT,
	LONGITUDE FLOAT,
	COUNTRY_NAME VARCHAR(250));

CREATE TABLE GLOBAL_LAND_TEMPS
(LT_ID INT PRIMARY KEY,
 LT_DATE DATE NOT NULL,
 AVG_TEMP FLOAT,
 AVG_TEMP_UN FLOAT,
 COUNTRY_ID VARCHAR(10),
 FOREIGN KEY (COUNTRY_ID) REFERENCES COUNTRY_LAT_LONG(COUNTRY_ID));
 
 CREATE TABLE GREEN_HOUSE_GASES
 (GH_ID INT PRIMARY KEY,
  COUNTRY VARCHAR(250),
  COUNTRY_ID VARCHAR(10),
  YEAR INT,
  OTHER_SOURCE FLOAT,
  WASTE FLOAT,
  INDUSTRY FLOAT,
  RESIDENTIAL FLOAT,
  TRANSPORT FLOAT,
  AGRICULTURE FLOAT,
  FORESTRY FLOAT,
  LAND_USE FLOAT,
  ENERGY FLOAT,
  FOREIGN KEY (COUNTRY_ID) REFERENCES COUNTRY_LAT_LONG(COUNTRY_ID));
  

  